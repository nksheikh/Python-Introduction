# Basic arithmetic
number =  1 + 2 * 3 / 4
print(number)

# Modulus
number = 11 % 3
print(number)

# Power
number = 2 ** 4
print(number)

# Concatenating Strings
str1, str2 = "Hello", "there"
print(str1 + " " + str2)

# Multiplying strings
str = "/|\\"
str = str * 10
print(str)

# Multiplying Lists
littleList1 = [1, 3, 5, 7 , 9]
littleList2 = [0, 2, 4, 6, 8]
bigList1 = littleList1 * 2
bigList2 = littleList1 + littleList2
print(bigList1)
print(bigList2)